package result.objectives.ecjProxy;

public class Utility {
    public static double calcMean(double[] data) {
        return Utility.calcMean(data);
    }

    public static double calcStandardDeviation(double[] data) {
        return Utility.calcStandardDeviation(data);
    }

    public static double maxValueInArray(double[] output) {
        return Math.max(output[0], output[1]);
    }
}
